import Head from "next/head";
import Layout from "../../components/layout";
import { getPost, getPostSlugs } from "../../lib/data";
import { useRouter } from "next/router";

export const getStaticPaths = async () => {
  const slugRes = await getPostSlugs();
  const slug = slugRes.posts;
  return {
    paths: slug.map((slug) => ({ params: { slug: slug.slug } })),
    fallback: false,
  };
};

export const getStaticProps = async ({ params }) => {
  const postItem = await getPost(params.slug);

  return {
    props: {
      postItem: postItem.posts[0],
    },
  };
};

const Home = ({ postItem }) => {
  const router = useRouter();

  if (router.isFallback) {
    return <div>Loading...</div>;
  }
  console.log(postItem);
  return (
    <Layout>
      <Head>
        <title>Awesome Portfolio</title>
        <meta name="description" content="Generated by create next app" />
        <link rel="icon" href="/favicon.ico" />
      </Head>

      <div>
        <h1>{postItem.title}</h1>
      </div>
    </Layout>
  );
};

export default Home;
